EJERCICIO 11
SELECT APENOM, NOMBRE, NOTA
FROM ALUMNOS, ASIGNATURAS, NOTAS
WHERE ALUMNOS.DNI = NOTAS.DNI
AND NOTAS.COD = ASIGNATURAS.COD
AND NOMBRE = 'FOL'
AND NOTA < 5
/
EJERCICIO 12
