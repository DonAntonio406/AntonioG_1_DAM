FROM ASIGNATURAS, NOTAS, ALUMNOS
WHERE ALUMNOS.POBLA = 'Madrid'
AND ALUMNOS.DNI = NOTAS.DNI
AND NOTAS.COD = ASIGNATURAS.COD
AND NOTA < 5;
EJERCICIO 13
